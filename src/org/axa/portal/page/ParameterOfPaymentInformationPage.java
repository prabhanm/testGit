package org.axa.portal.page;

public class ParameterOfPaymentInformationPage {
	
	private String paymentMode;
	private String cardOrStoreType;
	
	public String getPaymentMode() {
		return paymentMode;
	}
	public void setPaymentMode(String paymentMode) {
		this.paymentMode = paymentMode;
	}
	public String getCardOrStoreType() {
		return cardOrStoreType;
	}
	public void setCardOrStoreType(String cardOrStoreType) {
		this.cardOrStoreType = cardOrStoreType;
	}
	
	
}